lim <- function(x){
  x <- na.omit(x)
  X <- 1.2*max(x)
  return(c(0,X))
}

time <- function(x){
  return(1:length(x))
}

pop <- function(x,IP){
  X <- x;
  X[1] <- IP - x[1];
  for (i in 2:length(x)){
    X[i] <- X[i-1] - x[i];
  }
  return(X)
}

